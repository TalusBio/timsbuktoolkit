    #[test]
    fn parquet_schema_matches_golden_set() {
        use std::collections::BTreeMap;
        let batch = build_record_batch(&[]).expect("schema");
        let got: BTreeMap<String, (String, bool)> = batch
            .schema()
            .fields()
            .iter()
            .map(|f| {
                (
                    f.name().clone(),
                    (format!("{:?}", f.data_type()), f.is_nullable()),
                )
            })
            .collect();
        let want: BTreeMap<String, (String, bool)> = GOLDEN_SCHEMA
            .iter()
            .map(|(n, d, null)| (n.to_string(), (d.to_string(), *null)))
            .collect();

        let missing: Vec<_> = want.keys().filter(|k| !got.contains_key(*k)).collect();
        let extra: Vec<_> = got.keys().filter(|k| !want.contains_key(*k)).collect();
        assert!(missing.is_empty(), "columns missing vs golden: {missing:?}");
        assert!(
            extra.is_empty(),
            "unexpected new columns vs golden: {extra:?}"
        );
        for (name, want_ty) in &want {
            assert_eq!(&got[name], want_ty, "dtype/nullable mismatch for `{name}`");
        }
    }

    #[test]
    fn parsable_sequences_key_in_parquet_metadata() {
        let tmp = tempfile::NamedTempFile::new().expect("tmpfile");
        let path = tmp.path().to_path_buf();
        // Close the NamedTempFile so ResultParquetWriter can create the file
        // (File::create_new fails if the path already exists on some platforms,
        //  but NamedTempFile holds it open; drop it first).
        drop(tmp);
        {
            let writer = ResultParquetWriter::new(&path, 1024, true).expect("create writer");
            writer.close().expect("close");
        }
        let file = File::open(&path).expect("open");
        let reader = SerializedFileReader::new(file).expect("reader");
        let meta = reader.metadata().file_metadata();
        let kv_list = meta.key_value_metadata().expect("kv metadata present");
        let found: Vec<_> = kv_list
            .iter()
            .filter(|k| k.key == "parsable_sequences")
            .collect();
        assert_eq!(
            found.len(),
            1,
            "expected exactly one parsable_sequences key"
        );
        assert_eq!(
            found[0].value.as_deref(),
            Some("true"),
            "parsable_sequences value should be 'true'"
        );
    }
