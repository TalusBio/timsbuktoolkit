// ---------------------------------------------------------------------------
// Build a RecordBatch from a slice of FinalResult
// ---------------------------------------------------------------------------

/// Emit one result's columns into the sink (all scoring blocks, then the
/// post-model meta block).
fn emit_row(r: &FinalResult, sink: &mut ColSink) {
    r.scoring.push_columns(sink);
    r.result_meta().columns(sink);
    sink.end_row();
}

/// Build a `RecordBatch` from a slice of `FinalResult`.
///
/// **COMPILE-TIME SAFETY:** each block's `columns()` is generated by walking
/// the same field list that defines the block struct (see `score_block!`), so
/// a new field cannot silently skip the Parquet projection — there is no
/// hand-written destructure to fall out of sync.
pub fn build_record_batch(results: &[FinalResult]) -> std::io::Result<RecordBatch> {
    let mut sink = ColSink::new();
    if results.is_empty() {
        // Establish the schema from one sample row, then drop the data to
        // produce a zero-row batch (the writer builds its schema this way).
        emit_row(&FinalResult::sample(), &mut sink);
        sink.clear_rows();
    } else {
        for r in results {
            emit_row(r, &mut sink);
        }
    }

    let (fields, arrays) = sink.finish();
    let schema = Arc::new(Schema::new(fields));
    RecordBatch::try_new(schema, arrays)
        .map_err(|e| std::io::Error::new(std::io::ErrorKind::InvalidData, e))
}

// ---------------------------------------------------------------------------
// Buffered Parquet writer
// ---------------------------------------------------------------------------

pub struct ResultParquetWriter {
    writer: ArrowWriter<File>,
    buffer: Vec<FinalResult>,
    row_group_size: usize,
}

impl ResultParquetWriter {
    pub fn new(
        path: impl AsRef<Path>,
        row_group_size: usize,
        parsable_sequences: bool,
    ) -> std::io::Result<Self> {
        let file = match File::create_new(path.as_ref()) {
            Ok(f) => f,
            Err(err) => {
                tracing::error!("Failed to create file {:?}: {}", path.as_ref(), err);
                return Err(err);
            }
        };

        // Build schema from a zero-row batch
        let empty_batch = build_record_batch(&[])?;
        let schema = empty_batch.schema();

        let kv = vec![KeyValue {
            key: "parsable_sequences".to_string(),
            value: Some(parsable_sequences.to_string()),
        }];
        let props = WriterProperties::builder()
            .set_compression(Compression::SNAPPY)
            .set_key_value_metadata(Some(kv))
            .build();

        let writer =
            ArrowWriter::try_new(file, schema, Some(props)).map_err(std::io::Error::other)?;

        Ok(Self {
            writer,
            buffer: Vec::with_capacity(row_group_size),
            row_group_size,
        })
    }

    pub fn add(&mut self, result: FinalResult) -> std::io::Result<()> {
        self.buffer.push(result);
        if self.buffer.len() >= self.row_group_size {
            self.flush()?;
        }
        Ok(())
    }

    fn flush(&mut self) -> std::io::Result<()> {
        if self.buffer.is_empty() {
            return Ok(());
        }
        debug!("Flushing {} results to parquet", self.buffer.len());
        let batch = build_record_batch(&self.buffer)?;
        self.writer.write(&batch).map_err(std::io::Error::other)?;
        self.buffer.clear();
        Ok(())
    }

    pub fn close(mut self) -> std::io::Result<()> {
        self.flush()?;
        self.writer.close().map_err(std::io::Error::other)?;
        Ok(())
    }
}

